public class EntitasMahasiswa {
    //Deklarasi variabel private untuk Enkapsulasi
    private String user, namaa, nimm, hasil, tahun;
    private String password;
    private int tetapp, skss, variabel, nilai, studyy;


    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getNamaa() {
        return namaa;
    }

    public void setNamaa(String namaa) {
        this.namaa = namaa;
    }

    public String getNimm() {
        return nimm;
    }

    public void setNimm(String nimm) {
        this.nimm = nimm;
    }

    public int getTetapp() {
        return tetapp;
    }

    public void setTetapp(int tetapp) {
        this.tetapp = tetapp;
    }

    public int getSkss() {
        return skss;
    }

    public void setSkss(int skss) {
        this.skss = skss;
    }

    public int getVariabel() {
        return variabel;
    }

    public void setVariabel(int variabel) {
        this.variabel = variabel;
    }

    public int getNilai() {
        return nilai;
    }

    public void setNilai(int nilai) {
        this.nilai = nilai;
    }

    public String getHasil() {
        return hasil;
    }

    public void setHasil(String hasil) {
        this.hasil = hasil;
    }

    public String getTahun() {
        return tahun;
    }

    public void setTahun(String tahun) {
        this.tahun = tahun;
    }

    public int getStudyy() {
        return studyy;
    }

    public void setStudyy(int studyy) {
        this.studyy = studyy;
    }
  
    
}